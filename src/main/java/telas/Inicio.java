    private void carregarTabela(){

        DefaultTableModel model =
            (DefaultTableModel) TabeladeObras.getModel();

        model.setRowCount(0);

        ArtesDAO dao = new ArtesDAO();

        for(ArtesBean arte : dao.listar()){

            model.addRow(new Object[]{

                arte.getId(),
                arte.getNome(),
                arte.getDescricao(),
                arte.getPreco(),
                arte.getEstadodevenda()

            });

        }

        
    }

